/*E-1 素数判定*/
/*素数かどうかを判定する関数「isprime」を作成し、
2から1000までの素数を10列の表として表示するプログラムを作成しなさい。
isprime関数は判定する数字を引数に取り、
素数の場合は "1" を、そうでない場合は "0" を返す関数とする。
なお、実行例のように列を揃えて（10個単位で改行させて）表示させること
（ブラウザによっては、ずれて見える可能性がある）。*/

/*2022.7.25*/

#include<stdio.h>

int isprime( int );
int main()
{
    int i; //カウンタ
    int n=0;//素数の数
    int prime[]; 

    for( i=2 ; i<=1000 ; i++)
    {
        if( isprime( i )==1 ) 
        {
            prime[n] = i;
            n++;
        }
    }
    
}



